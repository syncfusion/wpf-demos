namespace syncfusion.chartdemos.wpf
{
    /// <summary>Represents a yearly record for stacked line charts.</summary>
    public class StackingLineChartModel
    {
        /// <summary>Gets or sets the year label.</summary>
        public string Year { get; set; }

        /// <summary>Gets or sets the value for the United Kingdom.</summary>
        public double UnitedKingdom { get; set; }

        /// <summary>Gets or sets the value for the United States.</summary>
        public double US { get; set; }

        /// <summary>Gets or sets the value for Cameroon.</summary>
        public double Cameroon { get; set; }

        /// <summary>Gets or sets the value for China.</summary>
        public double China { get; set; }
    }
}
