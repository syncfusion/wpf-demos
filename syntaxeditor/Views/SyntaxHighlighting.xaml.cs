using syncfusion.demoscommon.wpf;

namespace syncfusion.syntaxeditordemos.wpf
{
    /// <summary>
    /// Interaction logic for SyntaxHighlighting.xaml
    /// </summary>
    public partial class SyntaxHighlighting :DemoControl
    {
       public SyntaxHighlighting()
        {
            InitializeComponent();
        }

        public SyntaxHighlighting(string themename) : base(themename)
        {
            InitializeComponent();
        }

        protected override void Dispose(bool disposing)
        {
            this.Resources.Clear();

            if (this.edit != null)
            {
                this.edit = null;
            }

            if (this.Mainmenu != null)
                this.Mainmenu = null;

            if (this.Toolbar != null)
                this.Toolbar = null;

            base.Dispose(disposing);
        }
    }
}
